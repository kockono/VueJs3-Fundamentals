<template>
  <div>
    <div class="pokemon-container">
        <img 
            class="hidden-pokemon" 
            :src="imgSrc" 
            alt="pokemon">
        <img
            v-if="showPokemon" 
            class="fade-in"
            :src="imgSrc" 
            alt="pokemon">
    </div>
    <h3>Pokemon Picture</h3>
  </div>
</template>

<script>
export default {
    name: 'PokemonPicture',
    props:{
        pokemonId: {
            type: Number,
            required: true
        },
        showPokemon : {
            type: Boolean,
            required: true,
            default: false
        }
    },
    computed: {
        imgSrc() {
            return `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/dream-world/${this.pokemonId}.svg`;
        }
    }
}
</script>

